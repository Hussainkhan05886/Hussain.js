console.log (2 > 1); // true
console.log (2 < 1); // false
console.log (2 >= 1); // true
console.log (2 <= 1); // false  
console.log (2 == 1); // false
console.log (2 != 1); // true


console.log ("2" > 2); // false

// === is syntax for strict equality, it checks both value and type
console.log ("2" === 1); // false
console.log ("2" === "2"); // true

// the good method in compasion is both data type should be same, so use === instead of ==


// proffesional developers always write clean and readable code they avoid confusion and ambiguity, so they use === instead of ==